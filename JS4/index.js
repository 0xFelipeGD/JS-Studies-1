const resultado = prompt("Digite uma opção: a, b ou c");

switch (resultado) {
  case "a":
    alert("Opção A selecionada.");
    break;
  case "b":
    alert("Opção B selecionada.");
    break;
  case "c":
    alert("Opção C selecionada.");
    break;
  default:
    alert("Opção inválida.");
}
